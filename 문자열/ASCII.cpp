/*
	문자열 -> 정수 stoi, stol, stoll

	C++ string을 문자로 바꾸려면 
	stoi, stol, stoll 등등의 함수를 사용하면 된다.
	stoi: string -> int
	stol: string -> long
	stoll: string -> long long
	stof: string -> float
	stod: string -> double
	stold: string -> long double
	stoul: string -> unsigned long
	stoull: string -> unsinged long long
*/
