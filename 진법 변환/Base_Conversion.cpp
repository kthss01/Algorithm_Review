/*
	진법 변환 Base Conversion

	10진법 수 N을 B진법으로 바꾸려면 N이 0이 될때까지 나머지를 계속해서 구하면 된다.
	11을 3진법으로 바꾸는 방법
	11/3 = 3 ... 2
	3/3 = 1 ... 0
	1/3 = 0 ... 1
	11은 3진법 102이다.
*/
/*
	백준 문제
	11005 진법 변환 2
*/

/*
	진법 변환 Base Conversion

	B진법 수 N을 10진수로 바꾸려면 B^k을 곱하면서 더해가면 된다.
	3진법 수 102 = 1 * 3^2 + 0 * 3^1 + 2 * 3^0 = 11
*/
/*
	백준 문제
	2745 진법 변환
	1373 2진수 8진수
	1212 8진수 2진수
	2089 -2진수
*/

